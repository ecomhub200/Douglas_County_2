/**
 * CrashLens Batch Before/After Evaluation — KML Export
 * Generates KML file with color-coded placemarks and optional crash point layers.
 */
window.CL = window.CL || {};
CL.batchBA = CL.batchBA || {};

/**
 * Export batch results as KML file for Google Earth / GIS.
 * @param {boolean} includeCrashPoints - Include individual crash points as layers
 */
CL.batchBA.exportKML = function(includeCrashPoints) {
    var results = CL.batchBA.state.results.filter(function(r) { return r.status === 'success'; });
    if (results.length === 0) {
        alert('No results to export.');
        return;
    }

    var kml = '<?xml version="1.0" encoding="UTF-8"?>\n';
    kml += '<kml xmlns="http://www.opengis.net/kml/2.2">\n';
    kml += '<Document>\n';
    kml += '<name>Batch Before/After Study Results</name>\n';
    kml += '<description>Generated by CRASH LENS on ' + new Date().toLocaleDateString() + '. ' + results.length + ' countermeasure locations analyzed.</description>\n';

    // Define styles for effectiveness ratings
    // KML colors use AABBGGRR byte order (not AARRGGBB)
    var styles = [
        { id: 'highly-effective', color: 'ff16a34a', label: 'Highly Effective (CMF < 0.70)' },  // green
        { id: 'effective', color: 'ff0da365', label: 'Effective (CMF 0.70-0.90)' },              // light green
        { id: 'marginal', color: 'ff04a8ca', label: 'Marginal (CMF 0.90-1.00)' },               // yellow
        { id: 'ineffective', color: 'ff0c58ea', label: 'Ineffective (CMF 1.00-1.10)' },         // orange
        { id: 'negative', color: 'ff2626dc', label: 'Negative Impact (CMF > 1.10)' },            // red
        { id: 'before-crash', color: 'ff0066ff', label: 'Before-period crash' },                  // blue
        { id: 'after-crash', color: 'ff0000ff', label: 'After-period crash' }                     // red
    ];

    styles.forEach(function(s) {
        kml += '<Style id="' + s.id + '">\n';
        kml += '  <IconStyle><color>' + s.color + '</color><scale>1.2</scale>';
        kml += '<Icon><href>http://maps.google.com/mapfiles/kml/paddle/wht-circle.png</href></Icon>';
        kml += '</IconStyle>\n';
        kml += '  <LabelStyle><color>' + s.color + '</color><scale>0.8</scale></LabelStyle>\n';
        kml += '</Style>\n';
    });

    // Small dot styles for crash points
    kml += '<Style id="before-dot"><IconStyle><color>ffff8800</color><scale>0.5</scale>';
    kml += '<Icon><href>http://maps.google.com/mapfiles/kml/shapes/shaded_dot.png</href></Icon>';
    kml += '</IconStyle></Style>\n';
    kml += '<Style id="after-dot"><IconStyle><color>ff0055ff</color><scale>0.5</scale>';
    kml += '<Icon><href>http://maps.google.com/mapfiles/kml/shapes/shaded_dot.png</href></Icon>';
    kml += '</IconStyle></Style>\n';

    // Countermeasure Locations folder
    kml += '<Folder>\n<name>Countermeasure Locations</name>\n<open>1</open>\n';

    results.forEach(function(r) {
        var styleId = CL.batchBA._getKMLStyleId(r.cmf);
        var rating = CL.batchBA.getEffectivenessRating(r.cmf);
        var desc = '<![CDATA[' +
            '<b>Countermeasure:</b> ' + CL.batchBA._escapeXML(r.countermeasureType) + '<br>' +
            '<b>Install Date:</b> ' + r.installDate.toLocaleDateString() + '<br>' +
            '<b>Radius:</b> ' + r.radiusFt + ' ft<br>' +
            '<hr>' +
            '<b>Before Crashes:</b> ' + r.beforeTotal + '<br>' +
            '<b>After Crashes:</b> ' + r.afterTotal + '<br>' +
            '<b>Change:</b> ' + r.changePct.toFixed(1) + '%<br>' +
            '<b>CMF:</b> ' + (r.cmf !== null ? r.cmf.toFixed(3) : 'N/A') + '<br>' +
            '<b>CRF:</b> ' + (r.crf !== null ? r.crf.toFixed(1) + '%' : 'N/A') + '<br>' +
            '<b>Significant:</b> ' + (r.isSignificant ? 'Yes' : 'No') + '<br>' +
            '<b>Rating:</b> ' + rating.label + '<br>' +
            '<b>EPDO Before:</b> ' + Math.round(r.beforeEPDO) + ' / After: ' + Math.round(r.afterEPDO) +
            ']]>';

        kml += '<Placemark>\n';
        kml += '  <name>' + CL.batchBA._escapeXML(r.locationName) + '</name>\n';
        kml += '  <description>' + desc + '</description>\n';
        kml += '  <styleUrl>#' + styleId + '</styleUrl>\n';
        kml += '  <Point><coordinates>' + r.lng + ',' + r.lat + ',0</coordinates></Point>\n';
        kml += '</Placemark>\n';
    });

    kml += '</Folder>\n';

    // Optional crash point layers
    if (includeCrashPoints) {
        // Before-period crashes
        kml += '<Folder>\n<name>Before-Period Crashes</name>\n<visibility>0</visibility>\n';
        results.forEach(function(r) {
            if (r.beforeCrashes && r.beforeCrashes.length > 0) {
                kml += '<Folder><name>' + CL.batchBA._escapeXML(r.locationName) + ' (Before: ' + r.beforeCrashes.length + ')</name><visibility>0</visibility>\n';
                r.beforeCrashes.forEach(function(c) {
                    if (c.lat && c.lng) {
                        kml += '<Placemark><styleUrl>#before-dot</styleUrl>';
                        kml += '<Point><coordinates>' + c.lng + ',' + c.lat + ',0</coordinates></Point>';
                        kml += '</Placemark>\n';
                    }
                });
                kml += '</Folder>\n';
            }
        });
        kml += '</Folder>\n';

        // After-period crashes
        kml += '<Folder>\n<name>After-Period Crashes</name>\n<visibility>0</visibility>\n';
        results.forEach(function(r) {
            if (r.afterCrashes && r.afterCrashes.length > 0) {
                kml += '<Folder><name>' + CL.batchBA._escapeXML(r.locationName) + ' (After: ' + r.afterCrashes.length + ')</name><visibility>0</visibility>\n';
                r.afterCrashes.forEach(function(c) {
                    if (c.lat && c.lng) {
                        kml += '<Placemark><styleUrl>#after-dot</styleUrl>';
                        kml += '<Point><coordinates>' + c.lng + ',' + c.lat + ',0</coordinates></Point>';
                        kml += '</Placemark>\n';
                    }
                });
                kml += '</Folder>\n';
            }
        });
        kml += '</Folder>\n';
    }

    kml += '</Document>\n</kml>';

    // Download
    var blob = new Blob([kml], { type: 'application/vnd.google-earth.kml+xml' });
    var url = URL.createObjectURL(blob);
    var a = document.createElement('a');
    a.href = url;
    a.download = 'Batch_BA_Results_' + new Date().toISOString().split('T')[0] + '.kml';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
};

/** Map CMF to KML style ID */
CL.batchBA._getKMLStyleId = function(cmf) {
    if (cmf === null || cmf === undefined) return 'marginal';
    if (cmf < 0.70) return 'highly-effective';
    if (cmf < 0.90) return 'effective';
    if (cmf < 1.00) return 'marginal';
    if (cmf < 1.10) return 'ineffective';
    return 'negative';
};

/** Escape XML special characters */
CL.batchBA._escapeXML = function(str) {
    return String(str || '').replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
};

CL._registerModule('batch-ba/export-kml');
