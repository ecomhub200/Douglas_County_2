/**
 * CrashLens Batch Before/After Evaluation — PDF Report Export
 * Professional multi-page PDF matching the Standard Report design pattern.
 * Uses jsPDF + AutoTable with hex color palette, section accent bars, severity bars, and KPI cards.
 */
window.CL = window.CL || {};
CL.batchBA = CL.batchBA || {};

/**
 * Generate and download batch BA PDF report.
 */
CL.batchBA.exportPDF = function() {
    var s = CL.batchBA.state;
    if (!s.summary || s.results.length === 0) {
        alert('No results to export. Run the batch analysis first.');
        return;
    }

    var jsPDF = window.jspdf.jsPDF;
    var doc = new jsPDF('p', 'mm', 'letter');
    var pw = 215.9;
    var ph = 279.4;
    var m = 18;
    var cw = pw - m * 2;
    var footerH = 20;
    var headerH = 12;
    var safeBottom = ph - footerH - 5;

    // Professional color palette (matching Fatal & Speed tab)
    var C = {
        primary: '#1E3A5F', primaryLight: '#2563eb',
        text: '#374151', textLight: '#6b7280',
        fatal: '#991B1B', fatalLight: '#dc2626',
        serious: '#C2410C', moderate: '#f97316',
        minor: '#facc15', pdo: '#9CA3AF',
        success: '#065f46', successLight: '#16a34a',
        danger: '#dc2626', warning: '#ea580c',
        white: '#ffffff', gray: '#9CA3AF',
        border: '#e5e7eb', lightBg: '#f8fafc',
        secondary: '#7c3aed'
    };

    var successful = s.results.filter(function(r) { return r.status === 'success'; });
    var sum = s.summary;
    var dateStamp = new Date().toISOString().split('T')[0];
    var generatedDate = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });
    var epdoInfo = CL.core.epdo.getStateEPDOWeights(typeof STATE_FIPS !== 'undefined' ? STATE_FIPS : '_default');
    var pageNum = 0;
    var y = m + headerH;

    // === HELPER FUNCTIONS (matching Standard Report pattern) ===

    function hexToRgb(hex) {
        var result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
        return result ? { r: parseInt(result[1], 16), g: parseInt(result[2], 16), b: parseInt(result[3], 16) } : { r: 0, g: 0, b: 0 };
    }

    function setColor(hex) {
        var rgb = hexToRgb(hex);
        doc.setTextColor(rgb.r, rgb.g, rgb.b);
    }

    function setFill(hex) {
        var rgb = hexToRgb(hex);
        doc.setFillColor(rgb.r, rgb.g, rgb.b);
    }

    function cleanText(text) {
        if (!text) return '';
        return String(text).replace(/[\u2018\u2019]/g, "'").replace(/[\u201C\u201D]/g, '"')
            .replace(/[\u2013\u2014]/g, '-').replace(/[\u2026]/g, '...')
            .replace(/[\u00A0]/g, ' ').replace(/[^\x00-\x7F]/g, '').replace(/\s+/g, ' ').trim();
    }

    function drawPageHeader() {
        var rgb = hexToRgb(C.primary);
        doc.setFillColor(rgb.r, rgb.g, rgb.b);
        doc.rect(0, 0, pw, headerH, 'F');
        doc.setFontSize(9);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(255, 255, 255);
        doc.text('CRASH LENS', m, 8);
        doc.setFont('helvetica', 'normal');
        doc.text('Before/After Batch Evaluation Report', pw - m, 8, { align: 'right' });
    }

    function drawPageFooter(page, total) {
        doc.setDrawColor(200, 200, 200);
        doc.setLineWidth(0.3);
        doc.line(m, ph - 15, pw - m, ph - 15);
        doc.setFontSize(7);
        setColor(C.gray);
        var attr = typeof getReportAttribution === 'function' ? getReportAttribution() : 'CRASH LENS';
        doc.text('Generated by ' + attr + ' | ' + generatedDate, m, ph - 10);
        doc.text('Page ' + page + ' of ' + total, pw - m, ph - 10, { align: 'right' });
    }

    function newPage() {
        if (pageNum > 0) doc.addPage();
        pageNum++;
        drawPageHeader();
        y = m + headerH + 5;
    }

    function checkPageBreak(needed) {
        if (y + (needed || 20) > safeBottom) { newPage(); return true; }
        return false;
    }

    // Section title with left accent bar
    function addSectionTitle(title, color) {
        checkPageBreak(15);
        var rgb = hexToRgb(color || C.primary);
        doc.setFillColor(rgb.r, rgb.g, rgb.b);
        doc.rect(m, y - 4, 3, 10, 'F');
        doc.setFontSize(14);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(rgb.r, rgb.g, rgb.b);
        doc.text(cleanText(title), m + 6, y + 3);
        y += 12;
    }

    function addSubsectionTitle(title, color) {
        checkPageBreak(12);
        doc.setFontSize(11);
        doc.setFont('helvetica', 'bold');
        setColor(color || C.text);
        doc.text(cleanText(title), m, y);
        y += 7;
    }

    // KPI card (matching Standard Report)
    function drawKPICard(x, yy, width, height, value, label, color) {
        var rgb = hexToRgb(color);
        doc.setFillColor(rgb.r, rgb.g, rgb.b);
        doc.roundedRect(x, yy, width, height, 2, 2, 'F');
        doc.setFontSize(16);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(255, 255, 255);
        doc.text(String(value), x + width / 2, yy + height / 2 - 2, { align: 'center' });
        doc.setFontSize(7);
        doc.setFont('helvetica', 'normal');
        var lines = doc.splitTextToSize(cleanText(label), width - 4);
        doc.text(lines, x + width / 2, yy + height / 2 + 8, { align: 'center' });
    }

    // Severity bar (matching Standard Report)
    function drawSeverityBar(severity, maxWidth) {
        checkPageBreak(18);
        var total = (severity.K || 0) + (severity.A || 0) + (severity.B || 0) + (severity.C || 0) + (severity.O || 0);
        if (total === 0) return;
        var barH = 8;
        var curX = m;
        var segs = [
            { key: 'K', color: C.fatal, label: 'Fatal' },
            { key: 'A', color: C.fatalLight, label: 'Serious' },
            { key: 'B', color: C.moderate, label: 'Moderate' },
            { key: 'C', color: C.minor, label: 'Minor' },
            { key: 'O', color: C.pdo, label: 'PDO' }
        ];
        segs.forEach(function(seg) {
            var count = severity[seg.key] || 0;
            if (count > 0) {
                var w = (count / total) * maxWidth;
                setFill(seg.color);
                doc.rect(curX, y, w, barH, 'F');
                curX += w;
            }
        });
        doc.setDrawColor(200, 200, 200);
        doc.setLineWidth(0.3);
        doc.rect(m, y, maxWidth, barH, 'S');
        y += barH + 3;
        // Legend
        doc.setFontSize(7);
        var legendX = m;
        segs.forEach(function(seg) {
            var count = severity[seg.key] || 0;
            if (count > 0) {
                setFill(seg.color);
                doc.rect(legendX, y, 6, 4, 'F');
                setColor(C.text);
                doc.text(seg.label + ': ' + count, legendX + 8, y + 3);
                legendX += 35;
            }
        });
        y += 10;
    }

    // Effectiveness color for rating labels
    function ratingColor(label) {
        var map = { 'Highly Effective': C.successLight, 'Effective': '#65a30d', 'Marginal': '#ca8a04', 'Ineffective': C.warning, 'Negative Impact': C.danger };
        return map[label] || C.gray;
    }

    // ================================================================
    // PAGE 1: COVER PAGE
    // ================================================================
    newPage();
    y = 55;
    doc.setFontSize(26);
    doc.setFont('helvetica', 'bold');
    setColor(C.primary);
    doc.text('BATCH BEFORE/AFTER STUDY', pw / 2, y, { align: 'center' });
    y += 10;
    doc.setFontSize(14);
    doc.setFont('helvetica', 'normal');
    setColor(C.textLight);
    doc.text('Countermeasure Effectiveness Evaluation Report', pw / 2, y, { align: 'center' });

    // KPI Cards Row (5 cards, centered)
    y += 25;
    var cardW = 35;
    var cardH = 26;
    var cardGap = 5;
    var totalW = cardW * 5 + cardGap * 4;
    var cardX = (pw - totalW) / 2;

    drawKPICard(cardX, y, cardW, cardH, sum.totalAnalyzed, 'Locations Analyzed', C.primary);
    cardX += cardW + cardGap;
    var avgRedStr = sum.avgCrashReduction.toFixed(1) + '%';
    drawKPICard(cardX, y, cardW, cardH, avgRedStr, 'Avg Reduction', sum.avgCrashReduction > 0 ? C.success : C.danger);
    cardX += cardW + cardGap;
    drawKPICard(cardX, y, cardW, cardH, sum.avgCMF !== null ? sum.avgCMF.toFixed(3) : 'N/A', 'Average CMF', sum.avgCMF !== null && sum.avgCMF < 1 ? C.success : C.primaryLight);
    cardX += cardW + cardGap;
    var cpPdfLabel = sum.crashesPrevented >= 0 ? 'Net Prevented' : 'Net Increase';
    var cpPdfColor = sum.crashesPrevented >= 0 ? C.secondary : C.danger;
    drawKPICard(cardX, y, cardW, cardH, Math.abs(sum.crashesPrevented), cpPdfLabel, cpPdfColor);
    cardX += cardW + cardGap;
    drawKPICard(cardX, y, cardW, cardH, sum.significantPct.toFixed(0) + '%', 'Significant', C.primaryLight);

    y += cardH + 25;

    // Report details box
    setFill(C.lightBg);
    var rgb = hexToRgb(C.border);
    doc.setDrawColor(rgb.r, rgb.g, rgb.b);
    doc.roundedRect(m + 20, y, cw - 40, 55, 3, 3, 'FD');
    y += 12;
    doc.setFontSize(10);
    doc.setFont('helvetica', 'bold');
    setColor(C.text);
    doc.text('Report Details', pw / 2, y, { align: 'center' });
    y += 10;
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9);
    doc.text('Generated: ' + generatedDate, pw / 2, y, { align: 'center' });
    y += 6;
    doc.text('Analysis Radius: ' + s.globalRadiusFt + ' ft | Confidence Level: ' + (s.confidenceLevel * 100) + '%', pw / 2, y, { align: 'center' });
    y += 6;
    doc.text('EPDO Weights: ' + epdoInfo.name + ' (K=' + epdoInfo.weights.K + ', A=' + epdoInfo.weights.A + ', B=' + epdoInfo.weights.B + ', C=' + epdoInfo.weights.C + ', O=' + epdoInfo.weights.O + ')', pw / 2, y, { align: 'center' });
    y += 6;
    doc.text('Method: Empirical Bayes (simplified) | Significance: Two-tailed z-test (Poisson)', pw / 2, y, { align: 'center' });

    // ================================================================
    // PAGE 2: EXECUTIVE SUMMARY
    // ================================================================
    newPage();
    addSectionTitle('Executive Summary');

    // Key Takeaways box
    var takeaways = [];
    var improved = successful.filter(function(r) { return r.changePct < 0; }).length;
    var pctImproved = (improved / successful.length * 100).toFixed(0);
    takeaways.push(improved + ' of ' + successful.length + ' locations (' + pctImproved + '%) had fewer crashes after treatment.');
    if (sum.avgCrashReduction > 0) {
        takeaways.push('On average, crashes decreased by ' + sum.avgCrashReduction.toFixed(1) + '% across all locations.');
    } else if (sum.avgCrashReduction < 0) {
        takeaways.push('On average, crashes increased by ' + Math.abs(sum.avgCrashReduction).toFixed(1) + '% across all locations.');
    }
    var best = successful.slice().sort(function(a, b) { return a.changePct - b.changePct; })[0];
    if (best && best.changePct < 0) {
        takeaways.push('Best result: ' + cleanText(best.locationName) + ' saw a ' + Math.abs(best.changePct).toFixed(1) + '% reduction.');
    }
    if (sum.crashesPrevented > 0) {
        takeaways.push('A net estimated ' + sum.crashesPrevented + ' crashes were prevented across all treated locations.');
    } else if (sum.crashesPrevented < 0) {
        takeaways.push('There was a net increase of ' + Math.abs(sum.crashesPrevented) + ' crashes across all treated locations.');
    }
    var sigCount = successful.filter(function(r) { return r.isSignificant; }).length;
    if (sigCount > 0) {
        takeaways.push(sigCount + ' location(s) showed statistically significant improvement.');
    }

    // Draw takeaways box
    var takeawayH = 8 + takeaways.length * 5.5;
    checkPageBreak(takeawayH + 5);
    setFill('#eff6ff');
    var borderRgbTk = hexToRgb('#bfdbfe');
    doc.setDrawColor(borderRgbTk.r, borderRgbTk.g, borderRgbTk.b);
    doc.setLineWidth(0.4);
    doc.roundedRect(m, y, cw, takeawayH, 2, 2, 'FD');
    doc.setFontSize(9);
    doc.setFont('helvetica', 'bold');
    setColor('#1e40af');
    doc.text('Key Takeaways', m + 5, y + 5.5);
    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    setColor(C.text);
    var tkY = y + 11;
    takeaways.forEach(function(t) {
        doc.text('  *  ' + t, m + 5, tkY);
        tkY += 5.5;
    });
    y += takeawayH + 8;

    // Effectiveness distribution with colored badges
    addSubsectionTitle('Effectiveness Distribution');
    var effLabels = ['Highly Effective', 'Effective', 'Marginal', 'Ineffective', 'Negative Impact'];
    var effDescs = ['CMF < 0.70', 'CMF 0.70-0.90', 'CMF 0.90-1.00', 'CMF 1.00-1.10', 'CMF > 1.10'];
    var effData = effLabels.map(function(label, i) {
        return [label, effDescs[i], String(sum.byEffectiveness[label] || 0)];
    });

    var effColors = [C.successLight, '#65a30d', '#ca8a04', C.warning, C.danger];

    doc.autoTable({
        startY: y,
        head: [['Rating', 'CMF Range', 'Count']],
        body: effData,
        margin: { left: m, right: m },
        styles: { fontSize: 9, cellPadding: 3 },
        headStyles: { fillColor: hexToRgb(C.primary), textColor: [255, 255, 255] },
        alternateRowStyles: { fillColor: hexToRgb(C.lightBg) },
        columnStyles: { 0: { cellWidth: 50 }, 2: { halign: 'center', cellWidth: 25, fontStyle: 'bold' } },
        didParseCell: function(data) {
            if (data.column.index === 0 && data.section === 'body') {
                // Keep text dark for readability — accent bar drawn in didDrawCell
                data.cell.styles.textColor = hexToRgb(C.text);
                data.cell.styles.fontStyle = 'bold';
            }
        },
        didDrawCell: function(data) {
            if (data.column.index === 0 && data.section === 'body') {
                var idx = data.row.index;
                if (idx < effColors.length) {
                    var accentRgb = hexToRgb(effColors[idx]);
                    doc.setFillColor(accentRgb.r, accentRgb.g, accentRgb.b);
                    doc.rect(data.cell.x, data.cell.y, 2.5, data.cell.height, 'F');
                }
            }
        }
    });
    y = doc.lastAutoTable.finalY + 10;

    // Significance summary
    addSubsectionTitle('Statistical Significance');
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    setColor(C.text);
    doc.text(sum.significantCount + ' of ' + sum.totalAnalyzed + ' locations (' + sum.significantPct.toFixed(0) + '%) showed statistically significant crash changes at the ' + (s.confidenceLevel * 100) + '% confidence level.', m, y);
    y += 8;
    var cpPdfText = sum.crashesPrevented >= 0
        ? 'Net estimated crashes prevented: ' + sum.crashesPrevented + ' (sum of before minus after across all locations).'
        : 'Net crash increase: ' + Math.abs(sum.crashesPrevented) + ' (sum of before minus after across all locations).';
    doc.text(cpPdfText, m, y);
    y += 12;

    // Aggregated severity comparison
    if (successful.length > 0) {
        addSubsectionTitle('Aggregated Severity: Before vs After');
        var aggBefore = { K: 0, A: 0, B: 0, C: 0, O: 0 };
        var aggAfter = { K: 0, A: 0, B: 0, C: 0, O: 0 };
        successful.forEach(function(r) {
            ['K', 'A', 'B', 'C', 'O'].forEach(function(sev) {
                aggBefore[sev] += (r.beforeStats[sev] || 0);
                aggAfter[sev] += (r.afterStats[sev] || 0);
            });
        });

        doc.setFontSize(8);
        doc.setFont('helvetica', 'bold');
        setColor(C.fatalLight);
        doc.text('BEFORE', m, y);
        y += 2;
        drawSeverityBar(aggBefore, cw * 0.7);

        doc.setFontSize(8);
        doc.setFont('helvetica', 'bold');
        setColor(C.successLight);
        doc.text('AFTER', m, y);
        y += 2;
        drawSeverityBar(aggAfter, cw * 0.7);
    }

    // By countermeasure type (if multiple types)
    var typeKeys = Object.keys(sum.byType);
    if (typeKeys.length > 1) {
        checkPageBreak(40);
        addSubsectionTitle('Performance by Countermeasure Type');
        var typeRows = typeKeys.sort().map(function(t) {
            var info = sum.byType[t];
            var avgCmf = info.cmfCount > 0 ? (info.totalCMF / info.cmfCount).toFixed(3) : 'N/A';
            var avgChange = (info.totalChange / info.count).toFixed(1) + '%';
            return [t, String(info.count), avgCmf, avgChange];
        });
        doc.autoTable({
            startY: y,
            head: [['Countermeasure Type', 'Locations', 'Avg CMF', 'Avg Change']],
            body: typeRows,
            margin: { left: m, right: m },
            styles: { fontSize: 9, cellPadding: 2.5 },
            headStyles: { fillColor: hexToRgb(C.secondary), textColor: [255, 255, 255] },
            alternateRowStyles: { fillColor: hexToRgb(C.lightBg) },
            columnStyles: { 1: { halign: 'center', cellWidth: 25 }, 2: { halign: 'center', cellWidth: 25 }, 3: { halign: 'center', cellWidth: 25 } }
        });
        y = doc.lastAutoTable.finalY + 10;
    }

    // ================================================================
    // VISUAL ANALYSIS PAGE — Chart Images
    // ================================================================
    function captureChart(canvasId) {
        var canvas = document.getElementById(canvasId);
        if (!canvas || canvas.width === 0 || canvas.height === 0) return null;
        try { return canvas.toDataURL('image/png'); } catch (e) { return null; }
    }

    var chartBar = captureChart('batchBABarChart');
    var chartCMF = captureChart('batchBACMFChart');
    var chartSev = captureChart('batchBASeverityChart');
    var chartScatter = captureChart('batchBAScatterChart');
    var chartByType = captureChart('batchBACMFByTypeChart');
    var hasCharts = chartBar || chartCMF || chartSev || chartScatter;

    if (hasCharts) {
        newPage();
        addSectionTitle('Visual Analysis');

        var halfW = (cw - 6) / 2;  // 2-column with 6mm gap
        var chartH = 55;
        var chartTitleH = 8;

        // Row 1: Bar chart + CMF distribution
        if (chartBar) {
            doc.setFontSize(8);
            doc.setFont('helvetica', 'bold');
            setColor(C.primary);
            doc.text('Crash Rate: Before vs After', m, y + 4);
            try { doc.addImage(chartBar, 'PNG', m, y + chartTitleH, halfW, chartH); } catch (e) { /* skip */ }
        }
        if (chartCMF) {
            doc.setFontSize(8);
            doc.setFont('helvetica', 'bold');
            setColor(C.primary);
            doc.text('Safety Score Distribution', m + halfW + 6, y + 4);
            try { doc.addImage(chartCMF, 'PNG', m + halfW + 6, y + chartTitleH, halfW, chartH); } catch (e) { /* skip */ }
        }
        y += chartTitleH + chartH + 8;

        // Row 2: Severity comparison + Scatter plot
        checkPageBreak(chartTitleH + chartH + 10);
        if (chartSev) {
            doc.setFontSize(8);
            doc.setFont('helvetica', 'bold');
            setColor(C.primary);
            doc.text('Severity Comparison', m, y + 4);
            try { doc.addImage(chartSev, 'PNG', m, y + chartTitleH, halfW, chartH); } catch (e) { /* skip */ }
        }
        if (chartScatter) {
            doc.setFontSize(8);
            doc.setFont('helvetica', 'bold');
            setColor(C.primary);
            doc.text('Improvement Scatter Plot', m + halfW + 6, y + 4);
            try { doc.addImage(chartScatter, 'PNG', m + halfW + 6, y + chartTitleH, halfW, chartH); } catch (e) { /* skip */ }
        }
        y += chartTitleH + chartH + 8;

        // Optional: CMF by Type chart (full width, only if visible)
        if (chartByType) {
            checkPageBreak(chartTitleH + 45);
            doc.setFontSize(8);
            doc.setFont('helvetica', 'bold');
            setColor(C.primary);
            doc.text('Average Safety Score by Treatment Type', m, y + 4);
            try { doc.addImage(chartByType, 'PNG', m, y + chartTitleH, cw, 40); } catch (e) { /* skip */ }
            y += chartTitleH + 40 + 8;
        }
    }

    // ================================================================
    // PAGES 3+: Location table, details, methodology (in separate module)
    // ================================================================
    CL.batchBA._pdfCtx = {
        doc: doc, y: y, pageNum: pageNum,
        m: m, pw: pw, ph: ph, cw: cw, C: C,
        s: s, sum: sum, successful: successful, epdoInfo: epdoInfo,
        safeBottom: safeBottom, generatedDate: generatedDate, dateStamp: dateStamp,
        hexToRgb: hexToRgb, setColor: setColor, setFill: setFill, cleanText: cleanText,
        drawPageHeader: drawPageHeader, drawPageFooter: drawPageFooter,
        ratingColor: ratingColor,
        // Wrapper functions that sync ctx.y with the closure y
        newPage: function() {
            var ctx = CL.batchBA._pdfCtx;
            y = ctx.y; pageNum = ctx.pageNum;
            newPage();
            ctx.y = y; ctx.pageNum = pageNum;
        },
        checkPageBreak: function(needed) {
            var ctx = CL.batchBA._pdfCtx;
            y = ctx.y;
            var result = checkPageBreak(needed);
            ctx.y = y; ctx.pageNum = pageNum;
            return result;
        },
        addSectionTitle: function(title, color) {
            var ctx = CL.batchBA._pdfCtx;
            y = ctx.y;
            addSectionTitle(title, color);
            ctx.y = y;
        },
        addSubsectionTitle: function(title, color) {
            var ctx = CL.batchBA._pdfCtx;
            y = ctx.y;
            addSubsectionTitle(title, color);
            ctx.y = y;
        }
    };

    CL.batchBA._exportPDFDetails();
};

CL._registerModule('batch-ba/export-pdf');
